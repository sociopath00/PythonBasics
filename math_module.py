# import module
import math

# ceil -> returns next int value
print("Ceil Function: ")
print(math.ceil(3.75))
print(math.ceil(3.15))
print(math.ceil(3))

# floor -> returns previous int value
print("Floor Function")
print(math.floor(3.74))
print(math.floor(3.14))
print(math.floor(3))

# factorial
print("Factorial Function")
print(math.factorial(5))
print(math.factorial(0))

# exponent -> returns e raise to power x
print("Exp Function")
print(math.exp(3))

# log
print("Log Function")
print(math.log2(100))
print(math.log10(100))
print(math.log(100))

# pow
print("Power Function")
print(math.pow(5,3))
print(math.pow(5,2.5))

# sqrt
print("Square Root Function")
print(math.sqrt(25))
print(math.sqrt(100))

# constants
print(math.pi)
print(math.e)

