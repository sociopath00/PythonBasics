a = 7
b = 2

# addition
c = a+b
print("Addition: ", c)

# subtraction
d = a-b
print("Subtraction: ", d)

# Multiplication
e = a*b
print("Multiplication: ", e)

# Division
f = a/b
print("Division: ", f)

# Modulo operation
h = a%b
print("Remainder: ", h)

# Exponent
i = a**b
print("Power: ", i)

# Floor division
g = a//b
print("Floor Division: ",g)
